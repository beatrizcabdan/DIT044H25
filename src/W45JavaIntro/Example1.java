package W45JavaIntro; // W45JavaIntro is a package (folder)

// the class is called Main
class Main {

    // main is the entry point for any Java application
    // it needs to be public to be accessible from anywhere (more on W46)
    // it needs to be static, so it can be accessed without creating an object of the class first (more later on W45)
    // it returns void (nothing)
    public static void main(String[] args) {

        // the only thing this program does is print hello world!
        System.out.println("Hello World!");
    }
}
